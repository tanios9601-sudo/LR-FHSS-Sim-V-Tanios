close all;

% =========================================================
% LR-FHSS IMPERFECT SIC MODEL
% =========================================================
%
% Analytical Density Evolution
%
% Only imperfect SIC is considered:
% - SIC quality gamma
%
% No SIC limitation:
% L -> infinity
%
% =========================================================

% =========================================================
% PARAMETERS
% =========================================================

lambda_rate = 1/900;
th = 0.233472;

C = 35;
R = 3;

% SIC quality values

gamma_values = [ ...
    1.0 ...
    0.9 ...
    0.7 ...
    0.6 ...
    ];

% convergence

MAX_ITER = 10000;
EPS = 1e-12;

% =========================================================
% NODE RANGE
% =========================================================

N_values = 5000:1000:150000;

% =========================================================
% COLORS
% =========================================================

colors = [ ...
    0.2 0.5 0.8;
    0.2 0.7 0.3;
    0.9 0.5 0.1;
    0.6 0.3 0.8
    ];

% =========================================================
% FIGURE
% =========================================================

figure;
hold on;
grid on;

% =========================================================
% LOOP OVER GAMMA
% =========================================================

for idxG = 1:length(gamma_values)

    gamma = gamma_values(idxG);

    Eta_values = [];

    % -----------------------------------------------------
    % LOOP OVER N
    % -----------------------------------------------------

    for N = N_values

        % -------------------------------------------------
        % Offered load
        % -------------------------------------------------

        mu = lambda_rate * N;

        H = (2 * mu * R * th) / C;

        % -------------------------------------------------
        % Density evolution initialization
        % -------------------------------------------------

        p = 1.0;

        % -------------------------------------------------
        % Iterative SIC
        % -------------------------------------------------

        for iter = 1:MAX_ITER

            % Variable-node update

            q = p^(R-1);

            % ---------------------------------------------
            % Imperfect SIC model
            %
            % p = 1 - exp[-H(1-gamma+gamma*q)]
            % ---------------------------------------------

            p_new = 1 - exp( ...
                -H * ...
                (1 - gamma + gamma*q) ...
                );

            % ---------------------------------------------
            % Convergence test
            % ---------------------------------------------

            if abs(p_new - p) < EPS
                break;
            end

            p = p_new;

        end

        % -------------------------------------------------
        % Residual error probability
        % -------------------------------------------------

        p_min = p_new;

        % -------------------------------------------------
        % Header success probability
        % -------------------------------------------------

        Ph = 1 - p_min^R;

        % -------------------------------------------------
        % Efficiency
        % -------------------------------------------------

        %eta = H * Ph;
        eta = (H/R) * Ph;

        Eta_values = [Eta_values eta];

    end

    % =====================================================
    % THRESHOLD
    % =====================================================

    [eta_max, idx_max] = max(Eta_values);

    N_star = N_values(idx_max);

    fprintf('\n');
    fprintf('gamma = %.1f\n', gamma);
    fprintf('Threshold = %d nodes\n', N_star);
    fprintf('Maximum efficiency = %.4f\n', eta_max);

    % =====================================================
    % PLOT
    % =====================================================

    plot( ...
        N_values/1000, ...
        Eta_values, ...
        'LineWidth', 2, ...
        'Color', colors(idxG,:), ...
        'DisplayName', ...
        sprintf('\\gamma=%.1f | N*=%.0fk', ...
        gamma, N_star/1000) ...
        );

    % threshold line

    xline( ...
        N_star/1000, ...
        ':', ...
        'Color', colors(idxG,:), ...
        'HandleVisibility','off' ...
        );

end

% =========================================================
% FIGURE FORMAT
% =========================================================

xlabel('Number of end-devices (\times10^3)');
ylabel('\eta = H \times P_h');
title('LR-FHSS Imperfect SIC Model');

legend('Location','best');

