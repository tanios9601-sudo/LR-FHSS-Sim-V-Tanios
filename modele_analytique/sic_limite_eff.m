clear;
close all;

% =========================================================
% LR-FHSS LIMITED SIC MODEL
% =========================================================
%
% Analytical Density Evolution
%
% Only SIC limitation is considered:
%   - finite SIC depth L
%
% Perfect SIC:
%   gamma = 1
%
% =========================================================

% =========================================================
% PARAMETERS
% =========================================================

lambda_rate = 1/900;

th = 0.233472;

C = 35;

R = 3;

% SIC depth values

L_values = [2 3 4 5];

% convergence

MAX_ITER = 10000;

EPS = 1e-12;

% =========================================================
% NODE RANGE
% =========================================================

N_values = 5000:1000:150000;

% =========================================================
% COLORS
% =========================================================

colors = [
    0.2 0.5 0.8;
    0.2 0.7 0.3;
    0.9 0.5 0.1;
    0.6 0.3 0.8
];

% =========================================================
% FIGURE
% =========================================================

figure;
hold on;
grid on;

% =========================================================
% LOOP OVER L
% =========================================================

for idxL = 1:length(L_values)

    L = L_values(idxL);

    Eta_values = [];

    % -----------------------------------------------------
    % LOOP OVER N
    % -----------------------------------------------------

    for N = N_values

        % -------------------------------------------------
        % Offered load
        % -------------------------------------------------

        mu = lambda_rate * N;

        H = (2 * mu * R * th) / C;

        % -------------------------------------------------
        % Density evolution initialization
        % -------------------------------------------------

        p = 1.0;

        % -------------------------------------------------
        % Iterative SIC
        % -------------------------------------------------

        for iter = 1:MAX_ITER

            % Variable-node update

            q = p^(R-1);

            % ---------------------------------------------
            % Slot-node update
            %
            % Limited SIC only
            % ---------------------------------------------

            summation = 0;

            x = H * (1 - q);

            for k = 0:(L-1)

                summation = summation + ...
                    (x^k) / factorial(k);

            end

            p_new = 1 - exp(-H) * summation;

            % ---------------------------------------------
            % Convergence test
            % ---------------------------------------------

            if abs(p_new - p) < EPS
                break;
            end

            p = p_new;

        end

        % -------------------------------------------------
        % Residual error probability
        % -------------------------------------------------

        p_min = p_new;

        % -------------------------------------------------
        % Header success probability
        % -------------------------------------------------

        Ph = 1 - p_min^R;

        % -------------------------------------------------
        % Efficiency
        % -------------------------------------------------

        %eta = (H) * Ph;
        eta = (H/R) * Ph;

        Eta_values = [Eta_values eta];

    end

    % =====================================================
    % THRESHOLD
    % =====================================================

    [eta_max, idx_max] = max(Eta_values);

    N_star = N_values(idx_max);

    fprintf('\n');
    fprintf('L = %d\n', L);
    fprintf('Threshold = %d nodes\n', N_star);
    fprintf('Maximum efficiency = %.4f\n', eta_max);

    % =====================================================
    % PLOT
    % =====================================================

    plot( ...
        N_values/1000, ...
        Eta_values, ...
        'LineWidth', 2, ...
        'Color', colors(idxL,:), ...
        'DisplayName', ...
        sprintf('L=%d | N*=%.0fk', ...
        L, N_star/1000) ...
    );

    % threshold line

    xline( ...
        N_star/1000, ...
        ':', ...
        'Color', colors(idxL,:), ...
        'HandleVisibility','off' ...
    );

end

% =========================================================
% FIGURE FORMAT
% =========================================================

xlabel('Number of end-devices (\times10^3)');

ylabel('\eta = H \times P_h');

title('LR-FHSS Limited SIC Model');

legend('Location','best');

